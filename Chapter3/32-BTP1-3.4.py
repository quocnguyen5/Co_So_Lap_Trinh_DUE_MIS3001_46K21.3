toan = float(input())
ly = float(input())
hoa = float(input())

sum = (toan*2 + ly*3 + hoa)/6

if sum < 3:
    print('Kem')
elif 3 <= sum < 5:
    print('Yeu')
elif 5 <= sum < 6:
    print('Trung binh')
elif 6 <= sum < 7:
    print('Trung binh Kha')
elif 7 <= sum < 8:
    print('Kha')
elif 8 <= sum < 9:
    print('Gioi')
elif 9 <= sum < 10:
    print('Xuat sac')




